/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* USER CODE BEGIN 5 */

  initialize_all_motors(
    &htim2, TIM_CHANNEL_1,
    &htim2, TIM_CHANNEL_2,
    &htim2, TIM_CHANNEL_3,
    &htim2, TIM_CHANNEL_4,
    &htim8, TIM_CHANNEL_1,
    &htim8, TIM_CHANNEL_2,
    &htim8, TIM_CHANNEL_3,
    &htim8, TIM_CHANNEL_4
  );

  configure_micro_ros(&huart3);

  // micro-ROS app
  rcl_allocator_t allocator = rcl_get_default_allocator();

  //create init_options
  rclc_support_t support;
  rclc_support_init(&support, 0, NULL, &allocator);

  rcl_node_t stm32_node;
  rclc_node_init_default(&stm32_node, "stm32_node", "orca_auv", &support);

  rclc_executor_t executor = rclc_executor_get_zero_initialized_executor();
  unsigned int num_handles = KILL_SWITCH_NUM_HANDLES + MOTOR_PWM_ESC_NUM_HANDLES;
  printf("Debug: number of DDS handles: %u\n", num_handles);
  rclc_executor_init(&executor, &support.context, num_handles, &allocator);

  initialize_kill_switch_node(&support, &executor);
  initialize_motor_pwm_esc_ros(&stm32_node, &support, &executor);

  /* Infinite loop */
  for(;;)
  {
    if(is_kill_switch_closed())
    {
      stop_all_motors_pwm_output();
    }

    rclc_executor_spin_some(&executor, RCL_MS_TO_NS(1000));
    
    osDelay(1);
  }
  /* USER CODE END 5 */
}

